// Maps between mobile domain type codes and server primary/domain type codes.
// Enables cross-platform DTU interoperability.

import { DTU_TYPES } from '../utils/constants';
import type { DTUTypeCode } from '../utils/types';

// Server-side primary type codes (media/content — 0x01-0x0B)
