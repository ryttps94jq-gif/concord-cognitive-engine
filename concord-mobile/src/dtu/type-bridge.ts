  [DTU_TYPES.SHIELD_THREAT]: SERVER_DOMAIN_TYPES.SHIELD_THREAT,
  [DTU_TYPES.ECONOMY_TRANSACTION]: SERVER_DOMAIN_TYPES.ECONOMY_TRANSACTION,
  [DTU_TYPES.IDENTITY_ASSERTION]: SERVER_DOMAIN_TYPES.IDENTITY_ASSERTION,
  [DTU_TYPES.MESH_CONTROL]: SERVER_DOMAIN_TYPES.MESH_CONTROL,
  [DTU_TYPES.EMERGENCY_ALERT]: SERVER_DOMAIN_TYPES.EMERGENCY_ALERT,
  [DTU_TYPES.CREATIVE_WORK]: SERVER_PRIMARY_TYPES.MIXED,
  [DTU_TYPES.ATLAS_SIGNAL]: SERVER_DOMAIN_TYPES.ATLAS_SIGNAL,
  [DTU_TYPES.LINEAGE_REF]: SERVER_DOMAIN_TYPES.LINEAGE_REF,
